
package lista4exercicio1;


public class Lista4Exercicio1 {

    public static void main(String[] args) {
        
        System.out.println("Programa que imprime Olá, mundo 5x ");
        
        int cont = 5;
        
        while (cont > 0 ){
            System.out.println("Olá, mundo. \n ");
            cont --;
            
        }
            
            
        
        
    }
    
}
