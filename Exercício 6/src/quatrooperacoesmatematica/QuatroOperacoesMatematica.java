
package quatrooperacoesmatematica;


public class QuatroOperacoesMatematica {

   
    public static void main(String[] args) {
       
        System.out.println("Efetue as quatro operações matematicas com os respectivos números: 71 e 5");
        
        //soma
        int soma = 71 +5;
        
        //subtração
        
        int subtracao = 71 - 5;
        
        //multiplicação
        
        int multiplicacao = 71 * 5;
        
        //divisão
        
        float x = 71;
        float y = 5;
                
        float resultadoDivisao = x/y;
            
        System.out.println("Resultado da soma é: \n " + soma);
        System.out.println("Resultado da subtração é : \n " + subtracao);
        System.out.println("Resultado da multiplicação é: \n " + multiplicacao );
        System.out.println("Resultado da divisão é: \n " + resultadoDivisao);
    }
    
}
